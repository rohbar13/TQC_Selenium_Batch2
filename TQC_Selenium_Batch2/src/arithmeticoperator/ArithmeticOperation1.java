package arithmeticoperator;

public class ArithmeticOperation1 {
	public static void main(String[] args) {
		
		int x = 50;            //here = is assignment operator
		int y = 20;
		
		System.out.println(x+y);    // + is addition operator
		System.out.println(x-y);
		System.out.println(x*y);
		System.out.println(x/y);
		System.out.println(x%y);     // is modulus operator and return a remainder
	}
}
